static struct i2c_client *i2c_dev;

static inline int gc2155_write_reg(struct i2c_client *i2c, unsigned char addr, unsigned char value)
{
    unsigned char buf[2] = {addr, value};
    struct i2c_msg msg = {
        .addr = i2c->addr,
        .flags    = 0,
        .len    = 2,
        .buf    = buf,
    };

    int ret = i2c_transfer(i2c->adapter, &msg, 1);
    if (ret > 0)
        ret = 0;

    return ret;
}

static inline char gc2155_read_reg(struct i2c_client *i2c, unsigned char addr)
{
    char value = 0xff;

    struct i2c_msg msg[2] = {
        [0] = {
            .addr = i2c->addr,
            .flags    = 0,
            .len    = 1,
            .buf    = &addr,
        },
        [1] = {
            .addr = i2c->addr,
            .flags    = I2C_M_RD,
            .len    = 1,
            .buf    = &value,
        }
    };

    int ret = i2c_transfer(i2c->adapter, msg, 2);
    if (ret < 0)
        printk(KERN_ERR "gc2155: failed to read reg: %x\n", (int )addr);

    return value;
}

