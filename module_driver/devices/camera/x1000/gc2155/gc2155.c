static int gc2155_write_array(struct i2c_client *i2c,
                  const struct regval_list *vals)
{
    int ret;

    while ((vals->reg_num != 0xff) || (vals->value != 0xff)) {
        ret = gc2155_write_reg(i2c, vals->reg_num, vals->value);
        if (ret < 0) {
            return ret;
        }
        vals++;
    }
    return 0;
}

static int gc2155_read_array(struct i2c_client *i2c,
                               struct regval_list *vals)
{
    int ret;

    while ((vals->reg_num != 0xff) || (vals->value != 0xff)) {
        if (vals->reg_num == REG_PAGE) {
            ret = gc2155_write_reg(i2c, vals->reg_num, vals->value);
            if (ret < 0) {
                return ret;
            }
        } else {
            vals->value = gc2155_read_reg(i2c, vals->reg_num);
        }
        vals++;
    }

    return 0;
}

static int init_gpio(void)
{
    int ret;
    char gpio_str[10];

    if (reset_gpio != -1) {
        ret = gpio_request(reset_gpio,"gc2155_reset");
        if (ret) {
            printk(KERN_ERR "gc2155: failed to request rst pin: %s\n", gpio_to_str(reset_gpio, gpio_str));
            goto err_reset_gpio;
        }
    }

    if (power_gpio != -1) {
        ret = gpio_request(power_gpio,"gc2155_power");
        if (ret) {
            printk(KERN_ERR "gc2155: failed to request power pin: %s\n", gpio_to_str(power_gpio, gpio_str));
            goto err_power_gpio;
        }
    }

    if (pwdn_gpio != -1) {
        ret = gpio_request(pwdn_gpio,"gc2155_pwdn");
        if (ret) {
            printk(KERN_ERR "gc2155: failed to request pwdn pin: %s\n", gpio_to_str(pwdn_gpio, gpio_str));
            goto err_pwdn_gpio;
        }
    }

    return 0;
err_pwdn_gpio:
    gpio_free(power_gpio);
err_power_gpio:
    gpio_free(reset_gpio);
err_reset_gpio:

    return ret;
}

static void deinit_gpio(void)
{
    if (reset_gpio != -1)
        gpio_free(reset_gpio);

    if (power_gpio != -1)
        gpio_free(power_gpio);

    if (pwdn_gpio != -1)
        gpio_free(pwdn_gpio);
}

static int gc2155_sensor_stream_on(void)
{
    return gc2155_write_array(i2c_dev, gc2155_enable_output_regs);
}

static void gc2155_sensor_stream_off(void)
{
    gc2155_write_array(i2c_dev, gc2155_disable_output_regs);
}

void gc2155_sensor_power_off(void)
{
    if (reset_gpio != -1)
        gpio_direction_output(reset_gpio, 0);

    if (pwdn_gpio != -1)
        gpio_direction_output(pwdn_gpio, 1);

    if (power_gpio != -1)
        gpio_direction_output(power_gpio, 1);

    cim_disable_sensor_mclk();
}

int gc2155_sensor_power_on(void)
{
    int ret;

    cim_enable_sensor_mclk(24 * 1000 * 1000);

    if (power_gpio != -1) {
        gpio_direction_output(power_gpio, 1);
        msleep(50);
    }

    if(pwdn_gpio != -1){
        gpio_direction_output(pwdn_gpio, 1);
        msleep(10);
        gpio_direction_output(pwdn_gpio, 0);
        msleep(10);
    }

    if(reset_gpio != -1){
        gpio_direction_output(reset_gpio, 1);
        msleep(20);
        gpio_direction_output(reset_gpio, 0);
        msleep(20);
        gpio_direction_output(reset_gpio, 1);
        msleep(10);
    }

    /*
     * check and show product ID and manufacturer ID
     */
    ret = gc2155_read_array(i2c_dev, gc2155_chip_id_regs);
    if (ret < 0) {
        printk(KERN_ERR "gc2155: Failed to read chip id\n");
        goto err;
    }

    if ((gc2155_chip_id_regs[1].value != CHIP_ID_HIGH_GC2155) || (gc2155_chip_id_regs[2].value != CHIP_ID_LOW_GC2155)) {
        printk(KERN_ERR "gc2155: read sensor chip_id is error: %x\n",
                (int)gc2155_chip_id_regs[1].value);
        ret = -ENODEV;
        goto err;
    }

    ret = gc2155_write_array(i2c_dev, gc2155_init_regs);
    if (ret < 0) {
        printk(KERN_ERR "gc2155: failed to write init array %d", ret);
        goto err;
    }

    ret = gc2155_write_array(i2c_dev, gc2155_fmt_regs);
    if (ret < 0) {
        printk(KERN_ERR "gc2155: failed to write fmt array %d", ret);
        goto err;
    }

    /* 设置分辨率  */
    ret = gc2155_write_array(i2c_dev, gc2155_vga_regs);
    if (ret < 0) {
        printk(KERN_ERR "gc2155: failed to write window size %d", ret);
        goto err;
    }

    return 0;
err:
    gc2155_sensor_power_off();
    return ret;
}


static struct cim_sensor_config gc2155_sensor_config = {
    .device_name            = "gc2155",
    .cbus_addr              = I2C_ADDR,

    .cim_interface          = CIM_sync_mode,
    .index_byte0            = CIM_DVP_BYTE0,
    .index_byte1            = CIM_DVP_BYTE1,
    .index_byte2            = CIM_DVP_BYTE2,
    .index_byte3            = CIM_DVP_BYTE3,
    .hsync_polarity         = POLARITY_HIGH_ACTIVE,
    .vsync_polarity         = POLARITY_HIGH_ACTIVE,
    .data_sample_edge       = DVP_FALLING_EDGE,

    .sensor_info = {
        .width              = DEFAULT_WIDTH,
        .height             = DEFAULT_HEIGHT,
        .fmt                = SENSOR_PIXEL_FMT_YUYV8_2X8,
        .fps                = 25 << 16 | 1,
    },

    .ops = {
        .power_on           = gc2155_sensor_power_on,
        .power_off          = gc2155_sensor_power_off,
        .stream_on          = gc2155_sensor_stream_on,
        .stream_off         = gc2155_sensor_stream_off,
    },
};

static int sensor_gc2155_probe(struct i2c_client *client,
                 const struct i2c_device_id *id)
{
    int ret = init_gpio();
    if (ret)
        return ret;

    ret = cim_register_sensor(&gc2155_sensor_config);
    if (ret) {
        deinit_gpio();
        return ret;
    }

    return 0;
}

static int sensor_gc2155_remove(struct i2c_client *client)
{
    cim_unregister_sensor(&gc2155_sensor_config);
    deinit_gpio();
    return 0;
}

static const struct i2c_device_id gc2155_id_table[] = {
    { "md_sensor_gc2155", 0 },
    {},
};

static struct i2c_driver sensor_gc2155_driver = {
    .driver = {
        .name = "md_sensor_gc2155",
    },
    .probe = sensor_gc2155_probe,
    .remove = sensor_gc2155_remove,
    .id_table = gc2155_id_table,
};

static struct i2c_board_info sensor_gc2155_info = {
    .type = "md_sensor_gc2155",
    .addr = I2C_ADDR,
};

static int gc2155_sensor_init(void)
{
    if (i2c_bus_num < 0) {
        printk(KERN_ERR "gc2155: i2c_bus_num must be set\n");
        return -EINVAL;
    }

    int ret = i2c_add_driver(&sensor_gc2155_driver);
    if (ret) {
        printk(KERN_ERR "gc2155: failed to register i2c driver\n");
        return ret;
    }

    i2c_dev = i2c_register_device(&sensor_gc2155_info, i2c_bus_num);
    if (i2c_dev == NULL) {
        printk(KERN_ERR "gc2155: failed to register i2c device\n");
        i2c_del_driver(&sensor_gc2155_driver);
        return -EINVAL;
    }

    return ret;
}


static void gc2155_sensor_exit(void)
{
    i2c_unregister_device(i2c_dev);
    i2c_del_driver(&sensor_gc2155_driver);
}

module_init(gc2155_sensor_init);
module_exit(gc2155_sensor_exit);

