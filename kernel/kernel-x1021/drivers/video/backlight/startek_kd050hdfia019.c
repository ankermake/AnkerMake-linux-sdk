static void startek_kd050hdfia019_regulator_enable(struct startek_kd050hdfia019 *lcd)
{
	int ret = 0;
	struct lcd_platform_data *pd = NULL;

	pd = lcd->ddi_pd;
	mutex_lock(&lcd->lock);
	if(!regulator_is_enabled(lcd->lcd_vcc_reg)) {
		regulator_enable(lcd->lcd_vcc_reg);
	}

	msleep(pd->power_on_delay);
out:
	mutex_unlock(&lcd->lock);
}

static void startek_kd050hdfia019_regulator_disable(struct startek_kd050hdfia019 *lcd)
{
	int ret = 0;

	mutex_lock(&lcd->lock);
	if(regulator_is_enabled(lcd->lcd_vcc_reg)) {
		regulator_disable(lcd->lcd_vcc_reg);
	}
out:
	mutex_unlock(&lcd->lock);
}

