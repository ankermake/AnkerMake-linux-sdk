static int gc2145_s_ctrl(struct v4l2_ctrl *ctrl)
{
	struct v4l2_subdev *sd = to_sd(ctrl);
	struct gc2145_info *info = to_state(sd);

	switch (ctrl->id) {
		case V4L2_CID_BRIGHTNESS:
			return gc2145_s_brightness(sd, ctrl->val);
		case V4L2_CID_CONTRAST:
			return gc2145_s_contrast(sd, ctrl->val);
		case V4L2_CID_VFLIP:
			return gc2145_s_vflip(sd, ctrl->val);
		case V4L2_CID_HFLIP:
			return gc2145_s_hflip(sd, ctrl->val);
		case V4L2_CID_AUTOGAIN:
			/* Only set manual gain if auto gain is not explicitly
			   turned on. */
			if (!ctrl->val) {
				/* gc2145_s_gain turns off auto gain */
				return gc2145_s_gain(sd, info->gain->val);
			}
			return gc2145_s_autogain(sd, ctrl->val);
		case V4L2_CID_GAIN:
			return gc2145_s_gain(sd, ctrl->val);
		case V4L2_CID_ANALOGUE_GAIN:
			return gc2145_s_again(sd, ctrl->val);
		case V4L2_CID_EXPOSURE:
			return gc2145_s_exp(sd, ctrl->val);
	}
	return -EINVAL;
}

