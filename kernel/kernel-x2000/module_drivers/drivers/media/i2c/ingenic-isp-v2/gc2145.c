static int gc2145_fp_en(struct v4l2_subdev *sd, u32 val)
{
	struct gc2145_info *info = to_state(sd);
	if(val) {
		gpio_direction_output(info->fp.pin, info->fp.active_level);
		msleep(10);
	}
	else{
		gpio_direction_output(info->fp.pin, !info->fp.active_level);
		msleep(10);
	}
	return 0;
}

static int gc2145_core_reset(struct v4l2_subdev *sd, u32 val)
{
	struct gc2145_info *info = to_state(sd);

	if(val) {
		gpio_direction_output(info->fp.pin, !info->fp.active_level);
		msleep(10);
		gpio_direction_output(info->fp.pin, info->fp.active_level);
		msleep(10);
	}
	return 0;
}

